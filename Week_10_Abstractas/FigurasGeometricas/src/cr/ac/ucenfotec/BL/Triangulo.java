package cr.ac.ucenfotec.BL;

public class Triangulo extends Figura{
    private int base;
    private int altura;

    public Triangulo() {
        super();
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
}
